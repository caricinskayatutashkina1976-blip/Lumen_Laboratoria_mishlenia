export function getLessonByTopicId(topicId: string): Lesson | undefined {
  return lessons.find((l) => l.topicId === topicId);
}

export function getWhyNeededByTopicId(topicId: string): string[] {
  const lesson = getLessonByTopicId(topicId);
  return lesson?.whyNeeded ?? [
    'Эта тема помогает лучше понимать числа и решать задачи в жизни.',
    'Знания из математики пригождаются там, где нужно принимать решения.',
  ];
}

export function getSkillByTopicId(topicId: string): string {
  return getLessonByTopicId(topicId)?.skillTrained ?? 'Логическое мышление и внимательность';
}
