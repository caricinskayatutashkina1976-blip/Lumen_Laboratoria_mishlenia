export function getTrainingBySkill(skill: string): TrainingSkillData | undefined {
  return trainings.find((t) => t.id === skill);
}

export function isValidTrainingSkill(skill: string): skill is TrainingSkill {
  return trainings.some((t) => t.id === skill);
}
