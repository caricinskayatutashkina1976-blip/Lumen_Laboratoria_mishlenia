export type HomeworkStepResult =
  | 'empty'
  | 'cheat'
  | 'uncertain'
  | 'short'
  | 'accepted';

export interface HomeworkStepEvaluation {
  result: HomeworkStepResult;
  message: string;
}

const CHEAT_PATTERN =
  /дай ответ|реши полностью|напиши решение|просто скажи ответ|дай готовый|реши за меня|спиши|ответь сразу/;

const UNCERTAIN_PATTERN =
  /^(не знаю|не понимаю|не понял|хз|нет идеи|не могу|ничего)$/;

function normalize(text: string): string {
  return text.trim().toLowerCase().replace(/ё/g, 'е').replace(/\s+/g, ' ');
}

export function isCheatRequest(answer: string): boolean {
  return CHEAT_PATTERN.test(normalize(answer));
}

export function getCheatBlockMessage(): string {
  return 'Я помогу тебе решить, но не буду просто списывать за тебя. Давай начнём с первого шага: что известно в задаче?';
}

export function getEmptyAnswerMessage(): string {
  return 'Сначала попробуй написать свою мысль. Даже короткий ответ поможет понять, что уже ясно.';
}

export function getUncertainAnswerMessage(): string {
  return 'Так тоже бывает. Давай начнём с простого: найди в условии числа и выпиши их.';
}

