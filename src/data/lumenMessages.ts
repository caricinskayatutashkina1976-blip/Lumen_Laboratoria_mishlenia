export function getLumenMessage(situation: LumenSituation, index = 0): string {
  const messages = lumenMessages[situation];
  return messages[index % messages.length] ?? messages[0];
}

export function getTopicConfusedMessage(topicId?: string): string {
  if (topicId && topicStillConfused[topicId]) {
    return topicStillConfused[topicId];
  }
  return lumenMessages['not-understood'][0];
}

